package com.sudoplay.axion.stream;

public class AxionCharacterEncodingException extends RuntimeException {

  private static final long serialVersionUID = -1429309371309845014L;

  public AxionCharacterEncodingException() {
    super();
  }

  public AxionCharacterEncodingException(String message) {
    super(message);
  }

  public AxionCharacterEncodingException(Throwable cause) {
    super(cause);
  }

  public AxionCharacterEncodingException(String message, Throwable cause) {
    super(message, cause);
  }

}
