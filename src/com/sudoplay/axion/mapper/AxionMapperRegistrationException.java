package com.sudoplay.axion.mapper;

public class AxionMapperRegistrationException extends RuntimeException {

  private static final long serialVersionUID = -555884224983085552L;

  public AxionMapperRegistrationException() {
    super();
  }

  public AxionMapperRegistrationException(String message) {
    super(message);
  }

  public AxionMapperRegistrationException(Throwable cause) {
    super(cause);
  }

  public AxionMapperRegistrationException(String message, Throwable cause) {
    super(message, cause);
  }

}
