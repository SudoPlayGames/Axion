package com.sudoplay.axion.tag;

public class AxionIllegalNameChangeException extends RuntimeException {

  private static final long serialVersionUID = 894086908200994498L;

  public AxionIllegalNameChangeException() {
    super();
  }

  public AxionIllegalNameChangeException(String message) {
    super(message);
  }

  public AxionIllegalNameChangeException(Throwable cause) {
    super(cause);
  }

  public AxionIllegalNameChangeException(String message, Throwable cause) {
    super(message, cause);
  }

}
