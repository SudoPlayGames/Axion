package com.sudoplay.axion.adapter;

public class AxionAdapterRegistrationException extends RuntimeException {

  private static final long serialVersionUID = 6575639902716176697L;

  public AxionAdapterRegistrationException() {
    super();
  }

  public AxionAdapterRegistrationException(String message) {
    super(message);
  }

  public AxionAdapterRegistrationException(Throwable cause) {
    super(cause);
  }

  public AxionAdapterRegistrationException(String message, Throwable cause) {
    super(message, cause);
  }

}
