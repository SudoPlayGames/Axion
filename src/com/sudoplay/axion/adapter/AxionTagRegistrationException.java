package com.sudoplay.axion.adapter;

public class AxionTagRegistrationException extends RuntimeException {

  private static final long serialVersionUID = 6575639902716176697L;

  public AxionTagRegistrationException() {
    super();
  }

  public AxionTagRegistrationException(String message) {
    super(message);
  }

  public AxionTagRegistrationException(Throwable cause) {
    super(cause);
  }

  public AxionTagRegistrationException(String message, Throwable cause) {
    super(message, cause);
  }

}
