package com.sudoplay.axion.converter;

public class AxionConverterRegistrationException extends RuntimeException {

  private static final long serialVersionUID = 1389928396126697907L;

  public AxionConverterRegistrationException() {
    super();
  }

  public AxionConverterRegistrationException(String message) {
    super(message);
  }

  public AxionConverterRegistrationException(Throwable cause) {
    super(cause);
  }

  public AxionConverterRegistrationException(String message, Throwable cause) {
    super(message, cause);
  }

}
